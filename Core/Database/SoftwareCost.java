    private void writeFile() {
        addToListValues();

        try {
            // TODO add your handling code here:
            FileInputStream fis = new FileInputStream("Bao Cao.xls");
            wb = new HSSFWorkbook(fis);
            
            // sheet 4
            sheet = wb.getSheetAt(4);

            for (int r = 2; r <= 4; r++) {
                HSSFRow row = sheet.getRow(r);
                HSSFCell cell = row.getCell(3);
                cell.setCellValue(valueTAW.get(r - 2));
            }
            
            HSSFRow row4 = sheet.getRow(5);
            HSSFCell cell4 = row4.getCell(5);
            cell4.setCellValue(tawValue);
            
            
            // sheet 5
            sheet = wb.getSheetAt(5);
            for (int r = 3; r <= 13; r++) {
                if(r == 6 || r == 10)
                    continue;
                
                HSSFRow row = sheet.getRow(r);
                HSSFCell cell = row.getCell(2);
                if(r > 6 && r < 10){
                    cell.setCellValue(valueTBF.get(r - 4));
                } else if (r > 10) {
                    cell.setCellValue(valueTBF.get(r - 5));
                } else 
                    cell.setCellValue(valueTBF.get(r - 3));
            }
            
            HSSFRow row5 = sheet.getRow(14);
            HSSFCell cell5 = row5.getCell(5);
            cell5.setCellValue(ucValue);
            
            // sheet 6
            sheet = wb.getSheetAt(6);
            for (int r = 3; r <= 15; r++) {
                
                HSSFRow row = sheet.getRow(r);
                HSSFCell cell = row.getCell(4);
                cell.setCellValue(valueTCF.get(r - 3));
            }
            
            HSSFRow row6 = sheet.getRow(17);
            HSSFCell cell6 = row6.getCell(5);
            cell6.setCellValue(tcfValue);
            
            
            // sheet 7
            sheet = wb.getSheetAt(7);
            for (int r = 4; r <= 12; r++) {
                if(r == 9) continue;
                
                HSSFRow row = sheet.getRow(r);
                HSSFCell cell = row.getCell(14);
                if (r > 9)
                    cell.setCellValue(valueEF.get(r - 5));
                else 
                    cell.setCellValue(valueEF.get(r - 4));
            }
            
            HSSFRow row7 = sheet.getRow(14);
            HSSFCell cell7 = row7.getCell(15);
            cell7.setCellValue(efValue);
            
            HSSFRow row71 = sheet.getRow(16);
            HSSFCell cell71 = row71.getCell(16);
            cell71.setCellValue(pValue);
            
            // sheet 8
            sheet = wb.getSheetAt(8);
            for (int r = 2; r <= 6; r++) {
                
                HSSFRow row = sheet.getRow(r);
                HSSFCell cell = row.getCell(2);
                cell.setCellValue(valueH.get(r - 2).toString());
            }
            
            // sheet 1
            sheet = wb.getSheetAt(1);
            for (int r = 4; r <= 16; r++) {
                
                HSSFRow row = sheet.getRow(r);
                HSSFCell cell = row.getCell(3);
                cell.setCellValue(result.get(r - 4).toString());
            }
            

            fis.close();

            FileOutputStream fos = new FileOutputStream("Bao Cao.xls");
            wb.write(fos);

            wb.close();

            fos.close();

        } catch (IOException ex) {
            Logger.getLogger(SoftwareCost.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


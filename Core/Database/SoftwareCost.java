    private double toDoTAW() {
        simple = Double.valueOf(jTextFieldSimpleActor.getText());
        normal = Double.valueOf(jTextFieldMiddleActor.getText());
        complex = Double.valueOf(jTextFieldComplexActor.getText());

        return (simple * 1 + normal * 2 + complex * 3);
    }

    private double toDoTBF() {
        value11 = Integer.valueOf(jTextFieldB1.getText());
        value21 = Integer.valueOf(jTextFieldB2.getText());
        value31 = Integer.valueOf(jTextFieldB3.getText());
        value41 = Integer.valueOf(jTextFieldM1.getText());
        value51 = Integer.valueOf(jTextFieldM2.getText());
        value61 = Integer.valueOf(jTextFieldM3.getText());
        value71 = Integer.valueOf(jTextFieldT1.getText());
        value81 = Integer.valueOf(jTextFieldT2.getText());
        value91 = Integer.valueOf(jTextFieldT3.getText());

        return (value11 * 5 + value21 * 10 + value31 * 15 + value41 * 6 + value51 * 12 + value61 * 18 + value71 * 7.5 + value81 * 15 + value91 * 22.5);
    }

    private double toDoTCF() {
        double TFW = 0;
        valuetcf1 = Double.valueOf(jTextFieldTCF1.getText());
        valuetcf2 = Double.valueOf(jTextFieldTCF2.getText());
        valuetcf3 = Double.valueOf(jTextFieldTCF3.getText());
        valuetcf4 = Double.valueOf(jTextFieldTCF4.getText());
        valuetcf5 = Double.valueOf(jTextFieldTCF5.getText());
        valuetcf6 = Double.valueOf(jTextFieldTCF6.getText());
        valuetcf7 = Double.valueOf(jTextFieldTCF7.getText());
        valuetcf8 = Double.valueOf(jTextFieldTCF8.getText());
        valuetcf9 = Double.valueOf(jTextFieldTCF9.getText());
        valuetcf10 = Double.valueOf(jTextFieldTCF10.getText());
        valuetcf11 = Double.valueOf(jTextFieldTCF11.getText());
        valuetcf12 = Double.valueOf(jTextFieldTCF12.getText());
        valuetcf13 = Double.valueOf(jTextFieldTCF13.getText());

        TFW = 2 * valuetcf1 + 2 * valuetcf8 + valuetcf2 + valuetcf3 + valuetcf4 + valuetcf5 + valuetcf9 + valuetcf10 + valuetcf11 + valuetcf12 + valuetcf13
                + 0.5 * valuetcf6 + 0.5 * valuetcf7;
        return 0.6 + (0.01 * TFW);
    }

    private double toDoEF() {
        
            valueef1 = Double.valueOf(jTextFieldEF1.getText());
            valueef2 = Double.valueOf(jTextFieldEF2.getText());
            valueef3 = Double.valueOf(jTextFieldEF3.getText());
            valueef4 = Double.valueOf(jTextFieldEF4.getText());
            valueef5 = Double.valueOf(jTextFieldEF5.getText());    
            valueef6 = Double.valueOf(jTextFieldEF6.getText()); 
            valueef7 = Double.valueOf(jTextFieldEF7.getText());   
            valueef8 = Double.valueOf(jTextFieldEF8.getText());    

        double EFW = valueef1 * 1.5 + valueef2 * 0.5 + valueef3 + valueef4 * 0.5 + valueef5 + valueef6 * 2 - valueef7 - valueef8;
        return new BigDecimal(1.4 + (-0.03 * EFW)).setScale(2, BigDecimal.ROUND_HALF_UP).doubleValue();
    }

    private double toDoES() {
            valueef1 = Double.valueOf(jTextFieldEF1.getText());
            valueef2 = Double.valueOf(jTextFieldEF2.getText());
            valueef3 = Double.valueOf(jTextFieldEF3.getText());
            valueef4 = Double.valueOf(jTextFieldEF4.getText());
            valueef5 = Double.valueOf(jTextFieldEF5.getText());    
            valueef6 = Double.valueOf(jTextFieldEF6.getText()); 
            valueef7 = Double.valueOf(jTextFieldEF7.getText());   
            valueef8 = Double.valueOf(jTextFieldEF8.getText());    

        return getStability(valueef1) + getStability(valueef2) + getStability(valueef3) + getStability(valueef4) + getStability(valueef5)
                + getStability(valueef6) + getStability(valueef7) + getStability(valueef8);
    }

    public double toDoP() {
        if (toDoES() < 1) {
            return 48;
        }

        if (toDoES() >= 1.0 && toDoES() < 3) {
            return 32;
        }

        return 20;
    }

    private double getStability(double value) {
        if (value <= 0.0) {
            return 0.0;
        }
        if (value > 0.0 && value <= 1.0) {
            return 0.05;
        }
        if (value > 1.0 && value <= 2.0) {
            return 0.1;
        }
        if (value > 2.0 && value <= 3.0) {
            return 0.6;
        }

        return 1.0;
    }

    private double toDoH() {
        heSoLuong = Double.valueOf(jTextFieldH1.getText());
        luongToiThieu = Double.valueOf(jTextFieldH2.getText());
        soNgayLam = Integer.valueOf(jTextFieldH3.getText());
        soHLam = Integer.valueOf(jTextFieldH4.getText());
        
        double hValue = (heSoLuong * luongToiThieu) / (soNgayLam * soHLam);
        return new BigDecimal(hValue).setScale(0, BigDecimal.ROUND_HALF_UP).doubleValue();
    }

    private void showMe(boolean isImport) {
        if (!result.isEmpty()) {
            result.clear();
        }

        if (!isImport) {
            tawValue = toDoTAW();
        }
        result.add(tawValue);
        jLabeltaw.setText(String.valueOf(tawValue));

        if (!isImport) {
            ucValue = toDoTBF();
        }
        result.add(ucValue);
        jLabeltbf.setText(String.valueOf(ucValue));

        uucpValue = tawValue + ucValue;
        result.add(uucpValue);
        jLabeluucp.setText(String.valueOf(uucpValue));

        if (!isImport) {
            tcfValue = toDoTCF();
        }
        jLabeltcf.setText(String.valueOf(tcfValue));
        result.add(tcfValue);

        if (!isImport) {
            efValue = toDoEF();
        }

        jLabelef.setText(String.valueOf(efValue));
        result.add(efValue);

        aucpValue = new BigDecimal(uucpValue * tcfValue * efValue)
                .setScale(3, BigDecimal.ROUND_HALF_UP).doubleValue();
        jLabelaucp.setText(String.valueOf(aucpValue));
        result.add(aucpValue);

        if (!isImport) {
            pValue = toDoP();
        }
        jLabelp.setText(String.valueOf(pValue));
        result.add(pValue);

        eValue = new BigDecimal(aucpValue * 10 / 6).setScale(3,
                BigDecimal.ROUND_HALF_UP).doubleValue();
        jLabele.setText(String.valueOf(eValue));
        result.add(eValue);

        if (!isImport) {
            hValue = toDoH();
        }
        jLabelh.setText(String.valueOf(hValue));
        result.add(hValue);

        gValue = (long) new BigDecimal(1.4 * eValue * pValue * hValue)
                .setScale(0, BigDecimal.ROUND_HALF_UP).doubleValue();
        jLabelg.setText(String.format("%d%n", gValue));
        result.add(gValue);

        cValue = (long) new BigDecimal(gValue * 0.65).setScale(0,
                BigDecimal.ROUND_HALF_UP).doubleValue();
        jLabelc.setText(String.format("%d%n", cValue));
        result.add(cValue);

        tlValue = (long) new BigDecimal((gValue + cValue) * 0.06).setScale(0,
                BigDecimal.ROUND_HALF_UP).doubleValue();
        jLabeltl.setText(String.format("%d%n", tlValue));
        result.add(tlValue);

        jLabelgpm.setText(String.format("%d%n", (gValue + cValue + tlValue)));
        
        result.add(gValue + cValue + tlValue);
    }

